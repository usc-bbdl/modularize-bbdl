<center><h3>Myths and Monsters in Motor Control <br /><br /> </h3>

<p>21st Annual Conference of the Society for the Neural Control of Movement</p>

<p>Friday, April 29, 2011, 8:00 am - 10:15 am</p>

Chairs: Francisco J Valero-Cuevas 
<p></p>
Participants: Jason J Kutch, Gerald Loeb, and Evangelos Theodorou 
</center>

<p></p>

<b>Abstract</b>: Progress in the field of Sensorimotor Neuroscience hinges on a
clear and up-to-date understanding of its fundamental tenets. While the
validity of these tenets is routinely discussed and debated in the
literature, it is also necessary to present them to the community in a
manner that challenges all of us to affirm, update or revise them. This
is particularly true when challenges or revisions to these tenets hinge
on the details of specialized computational approaches, a nuanced
interpretation of multiple experimental findings, a thorough knowledge
of a vast literature, integration of recent findings, and/or careful
semantic distinctions. We have assembled a team of four investigators
with relevant expertise to critically review four fundamental tenets in
light of recent work: 

<p>1) Muscle redundancy; Valero-Cuevas</p>
<p>2) Henneman's Size Principle; Kutch</p> 
<p>3) Spinal Circuitry for Motor Behavior; Loeb</p>
<p>4) Optimal Control; Theodorou</p> 

<p>The Panel will progress systematically from the periphery inwards to update 
and revise our understanding of these four critical aspect of Sensorimotor
Neuroscience. We begin by discussing how the physics of tendon-driven
systems defines the problem to be solved by the nervous system - and go on
to show that we, in fact, have barely enough muscles for versatile
behavior. While these concepts do not change the fact that there exist
multiple solutions for "simple" tasks (i.e., with few constraints) and
in the close vicinity of a valid coordination pattern for a "complex"
task (i.e., with multiple constraints), they do strongly set the scope
of feasible solutions, necessitate "co-contraction" and "synergistic"
patterns independently of neural considerations - and make the system
vulnerable to loss or weakness of even single muscles.</p> 

<p> Next we transition to a reassessment of the physiological machinery that 
drives the recruitment of motor units - and
challenge the notion that recruitment system is as autonomous and robust
as is generally believed. A noninvasive innovative approach called
EMG-weighted averaging can reveal the profile the motor unit activation
pattern across the voluntary range (0-100% MVC) of an isometric
contraction. By linking this experimental approach with computational
models of motor unit activation we find that motor unit recruitment is
itself tuned in accordance with their direction of mechanical force
output.<p>

<p>Third, a computational assessment of the most thorough
compendium of spinal circuitry to date sheds light on the high-level
central drive necessary and sufficient to produce complex voluntary
movement - and suggest the system can robustly meet these demands in any
given individual with minimal tuning of the parameters of the circuitry.</p>

<p>The final part discusses the limitations of the optimal control framework and its
implications to neuromuscular systems. Optimal control requires accurate
knowledge of the dynamics and cost functions. Even though these
requirements may be satisfied in tasks such as small reaching movements,
they are strongly violated in learning scenarios or control tasks in
which the dynamics and rewards are not known or vary (i.e., the majority
of motor behaviors). We will provide an overview of other control
theoretic and machine learning approaches - seldom applied in our
field - motivated by these limitations and well suited to address them.
These include reinforcement learning and model predictive, robust, and
adaptive control.</p> 

