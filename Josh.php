<p>
Josh received his PhD in Biomedical Engineering from USC in 2012. He also received his BS in Mechanical Engineering from Georgia Tech in 2007 and his MS in Biomedical Engineering from USC in 2008. 
</p>

<p>
He was a graduate research assistant in the Brain-Body Dynamics Laboratory, and his research interests there included the theoretical analysis of bio-inspired tendon-driven systems, its implications for robotic and prosthetic hand design, and the study of human grasping and manipulation.
</p>

<p>
He is currently a post-doctoral research associate in the <a class="centerlink" href="http://www.mae.virginia.edu/muscle/">Multi-Scale Muscle Mechanics Lab </a>at the University of Virgnia. His research there is on analysis and optimization of <a class="centerlink" href="http://www.mae.virginia.edu/muscle/cleftpalate.html">cleft palate repair surgeries </a> with the aim of improving speech production in the clinical population.
</p>

<p>
He formerly served as the president of the <a class="centerlink" href="http://bbdl.usc.edu/Josh/USC_AlternativeCareersFlyer2011.pdf">USC Alternative Careers in Science and Engineering</a>, which recruits academic and industry professionals to be panelists at graduate student seminars on careers with a PhD.
</p>

<p>
His industrial experience includes internships at Southern Nuclear Company and Honda of America Manufacturing, as well as independent statistical consulting work. In addition, he has had extensive teaching assistant experience in several different classes: Advanced Studies of the Nervous System, Rehabilitation Engineering, Advanced Topics in Biomedical Systems, Introduction to Biomedical Engineering, and Biomechanics.
</p>



