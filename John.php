<strong> Research Description </strong>



<p>
I received my BS in Mechatronics Engineering with Honors from the National Autonomous University of Mexico (UNAM) in 2011. From UNAM, I have earned two Gabino Barreda Medals for Highest Academic Achievement and one Baz Prada Medal for Outstanding Social Service, along with other awards. I have worked on projects in diverse areas such as Computer Graphics, Computer Vision, Control Engineering, Prosthesis Design, Electronics Design, Real-Time System Development, among others.
</p>
<p>
In 2011, I was accepted into the Biomedical Engineering Department at the University of Southern California as a PhD student. I am currently working in the simulation, design and implementation of mechatronic systems in neuromuscular biomechanics in the Brain-Body Dynamics Laboratory and I actively collaborate with SangerLab at USC. As of late 2012, I now hold a MS in Biomedical Engineering from USC and I continue to pursue my PhD at this university.


