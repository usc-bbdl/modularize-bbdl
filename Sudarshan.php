<h3>
Sudarshan Dayanidhi <br /><br />
</h3>

<p>
I am a physical therapist currently pursuing my PhD in <a class="centerlink" href="http://pt.usc.edu/SubLayout.aspx?id=100&ekmensel=568fab5c_58_0_100_1"> Biokinesiology at USC</a>. Prior to my time here I worked in the Motion Analysis Laboratory at <a class="centerlink" href="http://www.shrinershq.org/Hospitals/Philadelphia/"> Shriners Hospitals</a> for Children in Philadelphia, performing gait and upper extremity analysis. My research work there and during my Master's at <a class="centerlink" href="http://www.drexel.edu/cnhp/rehab_sciences/welcome.asp"> Drexel University</a> was on scapular kinematics in children with typical development and in children with Brachial Plexus Birth Palsy. My collaborators were <a class="centerlink" href="http://www.jefferson.edu/facint/details.cfm?key=svd003"> Susan Duff</a> & <a class="centerlink" href="http://www.shrinershq.org/Hospitals/Philadelphia/about_us/Team/Shriners_Team_Scott_Hal_Kozin.aspx"> Scott Kozin</a>. I graduated with my Master's degree in physical therapy in 2003 and my thesis advisor was <a class="centerlink" href="http://biomechanics.uoregon.edu/obl/"> Andrew Karduna</a>. I obtained my Bachelor's degree in Physical therapy in 1999 from the <a class="centerlink" href="http://www.kem.edu/"> University of Mumbai</a>. I recently got a MS in Mechanical Engineering from University of Southern California (May 2010). 
</p>

<p>
I am very interested in the applications of biomechanical methods to understanding clinical problems. My research focuses on three primary areas: a) Developing a metric for measuring dexterity b) Understanding dexterity in children with Cerebral Palsy c) Understanding rehabilitation and mechanisms for improved dexterous function.   
</p>

