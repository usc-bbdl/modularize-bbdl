I received my Bachelor of Arts in Computational Biology from Pitzer College and have been working in the Brain Body Dynamics lab since May 2014. My passion lies in exploring difficult data problems and generating informative visualizations, with the intent of improving our understanding of movement. With this goal, I work with Python, R, and Matlab to apply machine learning, linear optimization, and statistical analysis to challenges in biokinesiology. My first project was to explore the multidimensional space of musculoskeletal force generations, where I generated the 'vectormap' - a new way to visualize feasible force capability for a lower limb. Preliminary results can be viewed at http://bbdl.usc.edu/vector_mapping.php
