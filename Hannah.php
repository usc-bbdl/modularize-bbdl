<p>
I received my BS in physical therapy from South Korea and obtained a Korean PT license in 2005. A field trip to the U.S. during my PT school influenced me to pursue further studies in the U.S., so I left Korea right after my graduation in 2005. I received an associate degree in science in Bellevue College, WA in 2008 and enrolled in a master's program in Teachers College, Columbia University in 2009. I obtained a PT license for New York State in 2009, and I worked at the Shield Institute, an early learning center, in Bronx, NY, as a part-time physical therapist for a year and 9 months. I worked with preschool aged children with autistic spectrum disorder and gross motor development delay. 
<br /><br />
During my master's program, I participated in CIT (constraint induced therapy) and HABIT (hand-arm bimanual intensive therapy) camps for children with hemiplegic cerebral palsy at the center for cerebral palsy (CP) research in TC, CU. My faculty advisor was Andy Gordon, and my master's thesis focused on finger individuation intervention during CIT and HABIT in children with hemiplegic CP. I received my MA in motor learning and control in May 2011. I enrolled a PhD program in Biokinesiology at USC and joined the brain-body dynamics lab in summer 2011. My research interests are activity-dependent and experience-dependent neuroplasticity, neuroimaging, and sensory-motor processing for hand manipulation. My goals for PhD are to investigate the mechanism of hand dexterity, using a transcranial magnetic stimulation (TMS) and develop a tool or intervention to enhance hand function for children with neurological disorders such as CP. 
</p>

