<h4> 
<big><u><center>Timeline for CONACyT-USC (CONUSC) PhD Fellowships at USC Viterbi School of Engineering - Fall Semester</center> </u></big>
</h4>


<br />
<br />
<b>Note: This process assumes having been admitted to the USC PhD program.  Admission deadlines are in December of the prior year. More information can be found <a class="centerlink" href="http://viterbi.usc.edu/admission/phd/" target = "blank"> here</a>. </b>

<br />
<br />
<br />

<p>
<b> YEAR ONE </b> <br />
<b> May </b> 
<li>Contact <a class="centerlink" href="http://globalization.usc.edu/international/mexico/" target = "blank">USC Mexico</a> office and <a class="centerlink" href="http://viterbi.usc.edu/academics/departments/" target = "blank">academic departments</a> for information about programs of study and admissions.</li>
<li>Visit an <a class="centerlink" href="http://www.educationusa.info/Mexico" target = "blank">EducationUSA</a> advising center and obtain information about the GRE & TOEFL exams. </li>
<li>Review the <a class="centerlink" href="http://www.ets.org/gre" target = "blank">GRE</a> and <a class="centerlink" href="http://www.ets.org/toefl" target = "blank">TOEFL</a> websites. </li>
<li>Schedule to take your exams between September and December.</li>
</p>
<br />

<p><b> June - August </b> 
<li>Establish contact with USC professors and investigators in the academic departments where you would like to study your PhD.</li>
<li>Conduct research with a professor or independently in order to test and demonstrate your research capability. </li>
<li>Study for GRE and TOEFL Exams. </p>
<br />
</p>




<p><b>September</b> 
<li>Study the application process and guidelines for your <a class="centerlink" href="http://viterbi.usc.edu/admission/phd/" target = "blank">department(s) of interest</a>.  A separate application is necessary for each department. </li>
<li>Decide on 2-3 professors in each academic department at USC with whom you would like to study your PhD. </li>
<li>Write a first draft of your Statement of Purpose, including specific information on what you would like to study, why, and with which 2-3 professors at USC. </li>
<li>Confirm with the USC Mexico office your intention to apply to the CONUSC Fellowship and ask for further guidance with your application. </li>
</p>
<br />

<p><b>September - December</b> 
<li>Study and take GRE and TOEFL exams.</li>
<li>Retake GRE exam in order to improve scores.</li>
</p>
<br />



<p><b>October</b>
<li>Refine your statement of purpose.  Ask a native English speaker to read it and give you feedback on grammar and style.  Ask an engineering professor or doctoral student to read it for content.  </li>
<li>Solicit 3 letters of recommendation from professors in engineering who are qualified to recommend your academic and research work in engineering. Ask for a recommendation letter from a professor at USC with whom you have studied over the summer. When asking for a letter of recommendation, give each recommender detailed instructions from the application, your statement of purpose, and a deadline.</li>
</p>
<br />

<p><b>November</b>
<li>Check with the professors who are writing your recommendation letters to make sure they have all of the information that they need and are aware of the deadline for sending their recommendations.</li>
<li>Polish your statement of purpose./li>
<li>Fill in the online application and send any supplemental materials.</li>
</p>
<br />

<p><b>December 1</b>
<li>Deadline to apply for admission to USC.</li>
<li>Notify USC Mexico that you have completed your application.</li>
<li>Verify that your GRE scores and TOEFL scores have been sent.</li>
</p>
<br />

<p><b>March</b>
<li>Admissions will inform you if your application is successful.</li>
</p>
<br />

<p>
<b>YEAR TWO</b><br />
<b>January- June</b>
<li>Prepare to finish classes and thesis or other graduation requirements before <b>June 1</b>.</li>
<li>Apply for a doctoral fellowship with <a class="centerlink" href="http://www.conacyt.gob.mx/FormacionCapitalHumano/Becas/Extranjero/Paginas/default.aspx" target = "blank">CONACyT</a>. There are separate deadlines depending on when you finish your degree requirements at your home institution. For more information please contact <a class="centerlink" href="mailto:avargas@conacyt.mx">Marco Vargas Bello</a> at CONACyT.</li>
<li>Contact the USC Mexico office and your academic department to let them know that you have applied for the CONACyT Fellowship, and inform them of the date by which you will be notified of acceptance by CONACyT.</li>
<li>Decide whether you will solicit a <a class="centerlink" href="http://travel.state.gov/visa/temp/types/types_1268.html" target = "blank">J-1 or an F-1 Visa</a>.</li>
<li>Review Visa guidelines and prepare any documents that you will need to provide (financial support requirement will be filled by winning the CONUSC Fellowship from both USC and CONACyT).</li>
</p>
<br />

<p><b>July - August</b>
<li>After receiving your CONACyT acceptance letter, send a copy of the letter, a copy of your passport, and current address and telephone numbers to your academic department and USC Mexico in order to generate your I-20 or DS-2019 needed for your Visa interview.</li>
<li>Ask USC Mexico when you can expect your necessary form, and when to schedule your Visa interview; schedule it with your nearest US Consulate.</li>
</p>
<br />

<p><b>August</b> - Classes begin 
</p>
<br />



