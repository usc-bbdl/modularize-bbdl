<p>
Applications are invited for two Postdoctoral positions with the T32 TREET Postdoctoral Training Program at the University of Southern California. An abstract highlighting the aims and methods of the T32 training program can be found <a href="Other/T32Abstract.pdf"><strong>here</strong></a>.
 <br /></p><p>
Individuals who qualify for the program and are interested in designing and implementing rehabilitation-focused clinical trials should submit their application materials to <a class="centerlink" href="mailto:huezo@usc.edu"> Patty Gutierrez </a> by no later than May 9, 2011.  Candidates should meet the following criteria:
</p>

<BLOCKQUOTE><li> Acquisition of a PhD or clinical doctorate degree in a field related to rehabilitation science within the last five years.  In the case of those with clinical doctorates, supporting evidence of significant research experience and strong scholarship are also required. </li></BLOCKQUOTE>
<BLOCKQUOTE><li>  Citizenship status consistent with NIH requirements. </li></BLOCKQUOTE>
<br />
<p>
In the selection process, we will consider the applicant’s history of productive involvement in research, degree of commitment to research on vulnerable and underrepresented groups such as children, women, minorities, and persons with disabilities, overall promise for a successful career as a scientist, the availability of respective research immersion opportunities, and demonstrated research interests relevant to those of one or more primary or specialty mentors. 
</p>
<BLOCKQUOTE><li> A CV or biosketch listing positions held, publications, fellowships, awards, and/or grant funding. </li></BLOCKQUOTE>
<BLOCKQUOTE><li>  A personal statement describing the candidate’s career goals and research interests, as well as his or her thoughts on primary or specialty mentors who would oversee his or her work. </li></BLOCKQUOTE>
<br />

<p>

Applicants interested in this position for clinical work related to sensorimotor integration or dexterous manipulation please also contact <a class="centerlink" href="mailto:valero@usc.edu">Prof. Valero-Cuevas</a>. See related work at the <a href="http://www.usc.edu/projects/rehab/research/optt_rerc/index.html">RERC </a>website.
