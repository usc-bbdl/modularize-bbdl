<strong> Research Description </strong>


<p>

I earned both my Bachelor&#39;s and Master&#39;s degrees in Biomedical Engineering from Louisiana Tech University in 2004 and 2006, respectively.  I was mentored by Dr. Walter Besio during my Master's research.  His research areas include the use of transcranial electrical stimulation via concentric ring electrodes to modulate neuronal activity, particularly during seizure.  My thesis used mathematical models to evaluate the effects of varied stimulation waveforms and electrode parameters during transcranial electrical stimulation.  These models were verified both theoretically and experimentally.

</p>

<p>
After graduation, I provided engineering support to the Neuroscience Motion Laboratory in the Human Adaptations and Countermeasures Division at NASA&#39;s Johnson Space Center.  
I was involved in the development of an integrated set of functional and physiological tests to determine how postflight changes in sensorimotor, cardiovascular and muscle physiology impact postflight functional performance.  This included human subject data collections and analysis as well as design, fabrication and implementation of electrical and mechanical hardware.  
I supported pre- and postflight data collections for five long-duration (ISS) and four short-duration (Shuttle) missions while at NASA.  

</p>
<p>


Since then, I&#39;ve worked as a project specialist in the Brain-Body Dynamics Laboratory. My current responsibilities include providing specialized research support in the development of research protocols, hardware and software development, and data collection and analysis.  
In the Fall of 2013, I resumed my PhD studies in Biomedical Engineering.  My research interests include understanding and quantifying the contributions of the brain and spinal cord to sensorimotor processing during dexterous manipulation and understanding the effects of gender/sex, age, and various clinical conditions. Additional information and a list of abstracts/publications can be obtained from my <a class="centerlink" href="http://bbdl.usc.edu/CVs/LawrenceCV.pdf"> CV</a>.  
</p>



