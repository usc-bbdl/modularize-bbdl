<strong> Brief Bio </strong>

<p>
I received my BS in Electrical Engineering from the University of Texas at Dallas in 2004. Afterwards, I became employed as a government contractor in White Sands Missile Range, NM where I worked on electronic countermeasures for various types of vehicles. After about four and half years, I decided to go back to school to learn how to combine electrical engineering with neuroscience and biology, specifically to work towards improving prostheses. In 2008, I was accepted into the Biomedical Engineering Department at the University of Southern California. In May 2009, I received my Master’s Degree and am now enrolled in the PhD program. 
</p>

<p>
Currently, I am developing a wireless data acquisition system that will transmit information from sensory devices (such as load cells, accelerometers, etc.) to a laptop via Bluetooth. The primary use of the device will be to capture force and acceleration data mounted on springs as a means to quantify a level of dexterity during a spring compression task. 
</p>
