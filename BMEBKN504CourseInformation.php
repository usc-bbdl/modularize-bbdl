<u>Topics</u>: 	At the end of the semester, students should be able to define and explain Neuromechanics as the evolutionary
co-adaptation of the nervous system and the body in the context of mechanical function by:
<ul style='margin-top:0in;margin-left:.5in' type=disc>
<li>Considering the basic organization of the sensorimotor neural system</li>
<li>Considering the organization of tendon-driven multi-joint systems</li>
<li>Outlining the link between the neural signals, muscle contraction, and biomechanical output</li>
<li>Defining the mechanical characteristics of muscles as force-generating units</li>
<li>Analyzing musculoskeletal forces that occur within the body</li>
<li>Understanding the concepts of muscle and kinematic redundancy</li>
<li>Appreciating the role of biomechanics in the clinical evaluation of disabilities</li>
<li>Describing the options used by the nervous system to control muscle force</li>
<li>Modeling, simulating, optimizing and animating a neuromuscular system</li>
<li>Designing experiments to evaluate the severity of a neuromuscular pathology</li>
</ul> 

