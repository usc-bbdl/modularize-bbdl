<br />

Purpose: To introduce basic and advanced engineering and neuroscience tools for analysis and simulation of motion
and force production of vertebrate limbs. These are very broad fields at the interface of biology, physiology, medicine and engineering.
Thus, the course emphasizes collaborative learning driven by carefully selected homework, readings, attendance of seminars, hands-on laboratory
experiences, and in-class exams. This will enable students to complete a semester-long project related to a neuromuscular system of their choice
using an engineering approach to the analysis and simulation.
 
<br> 
<br>
<br>
Topics: 	At the end of the semester, students should be able to:
<li>Identify the ways in which the human body interacts mechanically with the environment</li>
<li>Comprehend the concept of musculoskeletal forces that occur within the body</li>
<li>Perform analyses of musculoskeletal forces that act within the body</li>
<li>Appreciate the role of biomechanics in the clinical evaluation of disabilities</li>
<li>Explain how information is transmitted throughout the neuromuscular system</li>
<li>Outline the link between the neural signal and muscle contraction</li>
<li>Define the mechanical characteristics of muscles as force-generating units</li>
<li>Describe the options used by the nervous system to control muscle force</li>
<li>Examine the neuroanatomical basis and the role of afferent information in the operation of the system</li>
<li>Consider the organization and control of multi-joint systems</li>
<li>Understand the concept of muscle and kinematic redundancy</li>
<li>Model, simulate, optimize and animate a neuromuscular system</li>
<li>Analyze and interpret electromyograms</li>
<li>Design an experiment to evaluate the severity of a neuromuscular pathology</li>
 
 
