<p>
Thank you for your interest. I am always interested in outstanding, motivated and mature students from all over the world from the fields of Mechanics, Mathematics, Neuroscience, Physical Therapy, Computer Science and related areas to contribute to our lab.
</p>

<p>
Most of all, I enjoy students interested in bridging the Mathematics-Biology-Medicine divide (I put engineering inside the Mathematics). They must be very strong in at least one of those areas, and have interest in the other two.
</p>

<p>
<strong>Are you interested in our Lab?</strong> Choosing a PhD research direction is a big commitment in life. Maturity, understanding your goals in life, and motivation are as necessary to a successful PhD as is intellectual ability. Therefore, plan accordingly and only apply to labs that truly meet your goals.
</p>

<p>
We study how the sensorimotor processes in the brain control the body to produce versatile function in the context of the physical world.  We study this in the context of motor skill and disability in humans, as well as innovative machines. I emphasize combining (i) analytical, (ii) computational, and (iii) experimental, approaches, tools, and goals. For example, we develop computational modeling tools and approaches. We study analytical approaches to sensorimotor function. We build machines, robots and instrumentation. We do behavioral experimental work with healthy subjects, clinical populations, and people with uncommon skill and disabilities. We run clinical trials.
</p>

<p>
To apply to our lab you do not need to be excellent at all three (that is what the PhD should do), but you need to be outstanding in at least one, and proficient in a second.
</p>

<p>
<strong>What projects are available?</strong> The research must necessarily be associated with our ongoing funded projects (explore our website), but PhD students drive their own research because they are training to be independent thinkers and researchers. So, if our previous research excites you, we'll work together on finding a new project for you.
</p>

<p>
<strong><font color="#990000">PhD positions</strong> </font>are available either through Biomedical Engineering, Mechanical Engineering, and Computer Science Departments, the Division of Biokinesiology and Physical Therapy, and the Neuroscience Graduate Program, among others. I usually recruit about 1-2 new PhD students every year. <strong>Our admissions committees make the initial admission decision based on student quality, and individual professors make decisions for those admitted students based on matching interest and financial support (See below).</strong>
</p>

<p>
<strong>Informal inquiries for graduate studies.</strong> At USC, graduate admissions are handled by committee, not individually by faculty member. You are welcome to contact me directly if you have specific questions about research in the lab, or about how your own interests might line up with lab activities and plans (see below). If you decide to apply, you will need to do so formally through the graduate admissions offices of the departments, divisions and programs at USC. Once you have decided to apply, you should concentrate on making sure that your strengths are evident from your application. For example, if you have done any prior research, and especially if you have authored any international publications, be sure to point that out. Needless to say, admissions are very competitive at USC, and I can only recruit into the Lab very few of those very best students that are admitted.</p>

<p>
<strong>If you want to contact me.</strong> I get hundreds of e-mail inquiries per year. For me to be able to respond to you, I strongly suggest you include the following information. <br /><br />

•	Please explain succinctly why you are interested in our lab. To be specific, please let me know which of our papers you have read, and describe why at least one of them is of particular interest to you. <br /><br />
•	Please let me know which skill set is strongest in you, and why you want to build up the other two (remember: (i) analytical, (ii) computational, and (iii) experimental). <br /><br />
•	Please be very clear why you want to be in our lab as opposed to any of the thousands of other research/clinical labs in the world. <br /><br />
•	Please do not send me PDF’s, word files, etc, unless I request them. I rarely open files from people I do not know, and delete many of them because we have limited mail storage space. If you have a user-friendly webpage, feel free to point me to it. <br /><br />
<!--•	Avoid questions such as: Professor, what kind of work do you do? You’d be surprised how often I get that question, even though Google can answer it in milliseconds. <br /><br />-->

</p>

<p>
<strong>Formal applications.</strong> We look at hundreds of applications every year. Here are some ideas that might help your application stand out (at least for me): <br /><br />
•	When you write your statement of purpose, write clearly and at the beginning, what specific areas in which you are interested. <br /><br />
•	If you found specific Professors you might like to work with, mention their names explicitly. This helps assure they will pay close attention to your application. <br /><br />
•	Highlight any prior research experience you have. Try to have a research publication submitted to an international conference or journal, and provide a URL to it. Few students have this pre-PhD, so having a publication will definitely make you stand out. <br />
<br />
•	State your rank within your class and the rank of your university within your country, if not in the US. <br /><br />
•	Be sure to include references who can talk about your research ability, not just about your personality and motivation, although these are important too. <br /><br />
•	Practice, take and retake the GRE (and any other standardized tests like TOEFL, etc). Practice makes perfect, and you are competing against people with near-perfect scores. <br /><br />
•	Work on your GPA early in your undergraduate degree. Once again, you will be competing against people with near-perfect grades. <br /><br />

</p>

<p>
<strong>Financial support.</strong> Outstanding applicants deserve support, and the units of USC mentioned above have mechanisms for this, as well as my ongoing projects.
</p>

<p>
However, if you are able to obtain support (scholarships, etc) from a Foundation (American Heart Association, Hertz, Gates, etc.) or Government agency in the US (NSF Graduate Research Fellowship, NIH) or your country of origin (CONACYT in Mexico, DFG in Germany, EU in Europe, etc), be sure to do so. It demonstrates maturity and commitment to the PhD career. Once again, our admissions committee makes the initial decision based on student quality, and individual professors make decisions based on funding for those admitted students.
</p>

<p>
If you are a woman or member of an underrepresented minority in science, technology, engineering or mathematics, please make sure you mention that. USC is an equal opportunity institution. See the NSF <a class="centerlink" href="http://www.nsf.gov/news/news_summ.jsp?cntn_id=116094">link</a> if you want to learn more about this topic.
</p>

