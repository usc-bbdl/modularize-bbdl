<strong> Research Description </strong>




<p>
I received my Bachelor of Engineering in Mechanical Engineering from the American University of Beirut in 2009. After graduating I worked at an engineering consulting firm as a design engineer for HVAC projects in residential, institutional and hospitality and health care establishments. After two years, I arrived at USC and received a Master's Degree in Biomedical Engineering in 2012. I was enrolled in the PhD program in the same year and have been working on a collaboration project with both Prof. Valero-Cuevas and <a class="centerlink" href="http://kanso.usc.edu">Prof. Eva Kanso</a> from the Aerospace and Mechanical Engineering department at USC. My project has so far focused on a computational model describing slow and accurate finger motions. I am currently running human subject experiments with the goal of quantifying the discontinuities seen in such finger motions.
Being a pianist myself, I am interested in knowing more about accurate finger movement and strength, hand dexterity, and the control of hand and arm muscles to produce such motions.
